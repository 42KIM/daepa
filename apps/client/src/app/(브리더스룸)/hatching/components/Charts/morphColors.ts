// 모프 또는 형질 이름으로 색상 가져오기
export const getMorphOrTraitColor = (name: string, fallbackIndex: number = 0): string => {
  // 모프 색상에서 찾기
  if (MORPH_COLORS[name]) {
    return MORPH_COLORS[name];
  }

  // 형질 색상에서 찾기
  if (TRAIT_COLORS[name]) {
    return TRAIT_COLORS[name];
  }

  // 기본 차트 색상 폴백
  const DEFAULT_COLORS = [
    "#3b82f6",
    "#22c55e",
    "#f59e0b",
    "#ef4444",
    "#8b5cf6",
    "#ec4899",
    "#14b8a6",
    "#f97316",
    "#6366f1",
    "#84cc16",
  ];

  return DEFAULT_COLORS[fallbackIndex % DEFAULT_COLORS.length] || "#64748b";
};
